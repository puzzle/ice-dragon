      <p><span id="more-12033"></span></p>
<p>This is the technical part of a 2 part series. Read <a href="https://www.puzzle.ch/en/blog/articles/2018/11/14/reckless-puzzle-itc-at-the-lightning-network-hackday-in-new-york-city-1">part 1</a> if you are looking for a lighter read.</p>
<p>Note: This is not an introduction to the workings of the Lightning 
Network but a rather advanced and technical post. If you would like to 
learn about the basics, please check out <a href="https://lnroute.com/general-information/">this website</a> and DYOR.</p>
<p>Since the release of “<a href="https://lightning.network/lightning-network-paper.pdf">The Bitcoin Lightning Network</a>”
 paper in early 2015, the Lightning Network has been hailed as a 
solution that could finally bring sustainable scalability to Blockchains
 which are often described as hard to scale. The community around this 
“second layer” for cryptocurrencies has been growing at an astonishing 
pace, particularly after the <a href="https://bitinfocharts.com/de/comparison/bitcoin-transactionfees.html">fee crisis in late 2017</a>, when the urgent need for a higher Bitcoin transaction throughput became evident. The Puzzle ITC Lightning team joined the 4th <a href="https://lightninghackday.fulmo.org/">Lightning Hackday</a> in NYC to mingle with the core of this very community.</p>
<p>Read on, fellow Lightning pioneer, and relive this geeky Hackday with
 me! In this Blogpost, I write mostly about the insights I gathered from
 talking to people in the Lightning community. It’s worth it to share 
these fascinating topics because they could be helpful to other 
developers, too.</p>
<p>Learn more about some of the promising projects and savvy ideas happening at the cutting edge of innovation. <img draggable="false" class="emoji" alt="⚡" src="SampleBlogPost_files/26a1.svg"></p>

