        <p>New York City – at the cutting edge of innovation! Not at the
 New York Stock Exchange – but right next to it, in an ad hoc 
hackerspace dedicated to the Lightning Network. The place where crypto 
magic happened! We joined a bunch of reckless geeks working on a P2P 
payment processing revolution.</p>
      </div>
