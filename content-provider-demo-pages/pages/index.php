<h2>Insight #1: Creating inbound channel capacity</h2>
<p>At the moment, the Lightning Network only allows for single-funded 
channels. That is, if you want to create a channel, you have to pay the 
whole channel yourself. Once established, all the funds will be on your 
side of the channel. In other words, you will only be able to spend 
money over the Lightning Network, but you can’t receive any money as 
long as no other node is willing to open a channel with you and thus 
give you inbound capacity. This is a rather annoying situation if you 
want to create a shop since a shop mainly needs to be inbound capacity, 
right? Luckily, both-sided channels is one of the hottest topics in the 
LN protocols development and are probably soon to come. Until then, do 
we have other options than to wait for somebody to finally open a 
channel with us? Well, that’s what I discussed with <a href="https://twitter.com/alexbosworth">Alex Bosworth</a>.</p>
<p>Here’s what you can do: On zigzag.io you can send a Bitcoin over the 
Lightning Network and receive it back as an on-chain payment. It’s that 
easy. You now have inbound capacity.</p>
<p><em>Note: This is a custodial solution –&gt; you have to trust zigzag.io that they will actually send you money back. With <a href="https://bitcoinmagazine.com/articles/pay-bitcoin-mainnet-lightning-and-back-submarine-swaps-are-now-live/">Submarine Swaps</a>
 you can essentially do the same thing but in an atomic way. This way, 
either both transactions go through or none of them. No trust needed. … 
Ya gotta love that good ol‘ crypto magic!!</em></p>
<h2>Insight #2: Tor bitcoind, Tor LND, Tor everything!</h2>
<p>Privacy is a basic security necessity for Lightning nodes. After all,
 what your node is broadcasting to the whole world sounds something like
 this: “<em>Hey there! I am storing X Bitcoin right here [IP Address]! 
And you know what? I plan to leave that money here for a long time. If I
 weren’t, why would I have locked it in in the first place! So, take all
 the time you need coming up with a sophisticated attack. Once you’re 
set, just come here and pull it off. You know where you can find me (and
 my Bitcoin)!”</em></p>
<p>No need to point out that this is dangerous. Tor can help you at least obfuscate the <strong>where</strong>. With Tor, the message your node broadcasts sounds more like, “<em>I am storing X Bitcoin for a long time, but I’m not going to tell you either where I store it or who I am!</em>”<br>
So, I recommend you use Tor for both your Bitcoin as well as your 
Lightning node. Note: There is no such thing as perfect privacy. With 
Tor you may enhance your privacy but it is far from perfect. 
Furthermore, your real identity could be uncovered on other levels too. 
For example, through Blockchain analysis.<br>
Let me elaborate on the pros and cons that come with running your node over Tor:<br>
Pros:</p>
<ul>
<li>Enhanced privacy (as already stated above)</li>
<li>NAT punching. You run your node behind a router on which you cannot 
implement port forwarding and probably even behind a firewall that 
blocks all inbound connections? Tor will help you to circumvent both of 
these issues.</li>
<li>Dynamic IP addresses. They’re evil. Your nodes should have a static 
IP address to run smoothly. If your ISP wants to charge you extra for 
this, however, you might want to use Tor. It will run your node as 
hidden services and attribute a static .onion address to them.</li>
</ul>
<p>Cons:</p>
<ul>
<li>Network latency increases massively (more crucial for Lightning node than for Bitcoin node)</li>
</ul>
<p>There’s another annoying drawback to using Tor for Lightning nodes: 
Non-Tor nodes cannot establish connections with your node. In other 
words, you have to establish all the connections to these nodes. Once 
the connection is established, however, the connected node could open 
channels with your node by themselves.<br>
I learned from <a href="https://twitter.com/snyke">Christian Decker</a> 
that you can even run SSH over Tor. After he told me this, I got it 
running on my RaspiBolt. With this trick, I have now shutdown all the 
inbound connections to my RaspiBolt. Even though it sits behind a NAT 
and has a dynamic IP address, I can SSH to it from anywhere in the 
world. It also runs both bitcoind and LND seamlessly.</p>
<h2>Insight #3: Running a Lightning enabled Point of Sale <em>offline</em></h2>
<p>That’s right! You can run a Lightning enabled shop without it having 
any connection to the Lightning Network whatsoever! But how in the world
 … ?!</p>
<p>Let’s take a step back and talk Bitcoin first. Bitcoin offers this neat feature (standardized in <a href="https://github.com/bitcoin/bips/blob/master/bip-0032.mediawiki">BIP32</a>)
 that allows for so-called extended public keys. What it essentially 
allows us to do is derive public keys in a deterministic way from a 
given ext. pubKey. While the public keys may be derived from a computer 
A, you may have the corresponding extended private key on a computer B, 
which can be used to derive all the corresponding private keys even if 
computer A and computer B are completely isolated from each other.</p>
<p><a href="https://www.puzzle.ch/wp-content/uploads/2018/11/ExtendedKeyDerivation.png" data-rel="lightbox-image-0" data-rl_title="" data-rl_caption="" title=""><img class="size-full wp-image-12038 aligncenter" src="https://www.puzzle.ch/wp-content/uploads/2018/11/ExtendedKeyDerivation.png" alt=""></a></p>
<p>In other words, computer A could generate all your Bitcoin addresses,
 but it could not create any corresponding privKeys and thus not spend 
any of the received Bitcoin. This feature can come in handy as a 
protection for your private keys. Let’s say you run a web shop. You 
could generate all your Bitcoin addresses in that web shop while 
generating all the corresponding private keys on a separate, offline 
computer. So, if somebody were to break into your web shop, they could 
still not steal your Bitcoin because there are no privKeys to be found.</p>
<p>Now, back to Lightning. I learned from <a href="https://www.youtube.com/user/renepickhardt">René Pickhardt</a>
 how you can pull off a similar trick on Lightning. In Lightning, if 
Alice wants to pay Bob, Bob will have to create a (usually randomly 
generated) secret that we will henceforth call preimage. He will then 
hash this preimage and send it to Alice. The inner working of the 
Lightning Network is such that Alice will only learn about the preimage 
as soon as her payment goes through. The payment is atomic in such a way
 that if Bob were to withhold the preimage from Alice, the payment would
 not go through. Conclusion: If Alice knows the preimage, the payment 
went through.</p>
<p><a href="https://www.puzzle.ch/wp-content/uploads/2018/11/LightningMechanics.png" data-rel="lightbox-image-1" data-rl_title="" data-rl_caption="" title=""><img class="size-full wp-image-12039 aligncenter" src="https://www.puzzle.ch/wp-content/uploads/2018/11/LightningMechanics.png" alt=""></a></p>
<p>Here’s the hack: What if we created the preimage in a deterministic 
way? We could separate the Lightning node from the Point of Sale while 
creating the exact same invoice (using the same preimage) on both 
systems. The payment would still be handled by the Lightning node, but 
the PoS would serve the hashed preimage (i.e. the invoice) to the 
customer. Afterwards, the PoS could also prove that the payment went 
through by simply requesting the preimage from the customer.</p>
<p><img class="size-full wp-image-12062 aligncenter" src="SampleBlogPost_files/OfflinePoS_QuickFix.png" alt=""></p>
<p>For the preimage derivation scheme, we propose the use of a reverse hash chain:</p>
<p><a href="https://www.puzzle.ch/wp-content/uploads/2018/11/Bildschirmfoto-vom-2018-11-21-12-30-52.png" data-rel="lightbox-image-2" data-rl_title="" data-rl_caption="" title=""><img class="alignnone size-full wp-image-12042" src="https://www.puzzle.ch/wp-content/uploads/2018/11/Bildschirmfoto-vom-2018-11-21-12-30-52.png" alt=""></a></p>
<p>Note that with this solution, the Lightning node needs to be aware of
 each product’s price. If not, an attacker could manipulate the invoice 
and pay a much smaller price than what actually was demanded from the 
PoS, yet still receive the preimage thus convincing the PoS that the 
product is paid.</p>
<p>This can get a little tricky if you have multiple products and the 
Lightning node does not know which product the customer is paying for. 
In this case, you could hash some information into the preimage on the 
PoS. The Lightning node could then later on find out what product the 
customer chose by trial and error (bruteforce).<br>
Here’s what a solution could look like:</p>
<p><a href="https://www.puzzle.ch/wp-content/uploads/2018/11/Bildschirmfoto-vom-2018-11-21-12-31-56.png" data-rel="lightbox-image-3" data-rl_title="" data-rl_caption="" title=""><img class="alignnone size-full wp-image-12043" src="https://www.puzzle.ch/wp-content/uploads/2018/11/Bildschirmfoto-vom-2018-11-21-12-31-56.png" alt=""></a></p>
<p>As soon as the lightning node receives a hashed preimage, it would 
then try to find out what product the customer bought by iterating 
through the product ids. To make this task easier, you could provide the
 product id as a payment message. Note that a payment message alone is 
not failproof since this information could be changed by malicious 
customers.</p>
<p>We are considering implementing this solution on our PoS for both 
removing complexity from it and increasing its security. The main 
sticking point is that with this solution, it is not sufficient to only 
have information flowing from the PoS to the customer’s device. That 
information also needs to flow in the opposite direction.</p>
<h2>Insight #4: Sending a payment without invoice</h2>
<p>René furthermore explained how you could send a payment over the 
Lightning Network without the need for any invoice at all. If you want 
to know more about this, check out his <a href="https://www.youtube.com/watch?v=Dwl-0cY6KkU" data-rel="lightbox-video-10">video</a> about it.</p>
<h2>Please sir, when moon?</h2>
<p>&nbsp;</p>
<p>As the Bitcoin community and cryptocurrency space is increasingly 
undermined by greed and get-rich-quick schemes, the Hackerspace provided
 a two-day retreat from all the noise. We did not once hear anything 
about the Bitcoin price! Instead, we witnessed people sharing clever 
ideas and tinkering on solutions with tremendous potential to shape a 
promising future for mankind.<br>
That being said, I’m sorry, sir. I don’t know when we’ll see lightnings 
on the moon. But, I’m pretty positive that Earth will get struck by 
Lightning quite a bit ;-).</p>
